package udemySection03;

public class Primitive_Max_Min {
    public static void display(){
        // Primitive data type: Max and Min value

        System.out.println("Byte Minimum Number: " + Byte.MIN_VALUE);
        System.out.println("Byte Maximum Value: " + Byte.MAX_VALUE);

        System.out.println("Short Minimum Number: " + Short.MIN_VALUE);
        System.out.println("Short Maximum Value: " + Short.MAX_VALUE);

        System.out.println("Integer Minimum Number: " + Integer.MIN_VALUE);
        System.out.println("Interger Maximum Value: " + Integer.MAX_VALUE);

        System.out.println("Long Minimum Number: " + Long.MIN_VALUE);
        System.out.println("Long Maximum Value: " + Long.MAX_VALUE);

        System.out.println("Float Minimum Number: " + Float.MIN_VALUE);
        System.out.println("Float Maximum Value: " + Float.MAX_VALUE);

        System.out.println("Double Minimum Number: " + Double.MIN_VALUE);
        System.out.println("Double Maximum Value: " + Double.MAX_VALUE);
    }
}
